/**
 * op_add - function to add two integers
 * @a: integer 1
 * @b: integer 2
 * Return: the sum of @a and @b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to subtract two integers
 * @a: integer 1
 * @b: integer 2
 * Return: the difference of @a and @b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply two integers
 * @a: integer 1
 * @b: integer 2
 * Return: the product of @a and @b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide two integers
 * @a: integer 1
 * @b: integer 2
 * Return: the division of @a and @b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to get remainder between two integers
 * @a: integer 1
 * @b: integer 2
 * Return: the remainder of @a and @b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
