/**
 * _putchar - function to print single characters in terminal 
 */

char _putchar(int ch);

/**
 * _isupper - function to check for uppercase letters
 *
 * Returns 1 if uppercase 0 if otherwise
 */

int _isupper(int c);

/**
 * _isdigit - function to check if value is a digit
 * 
 * Returns 1 if digit 0 if otherwise
 */

int _isdigit(int c);

/**
 * mul - function to multiply two integers
 *
 * Returns the result of the multiplied values
 */

int mul(int a, int b);
